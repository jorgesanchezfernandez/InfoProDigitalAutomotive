using infoproDigitalTechTask.Data;
using infoproDigitalTechTask.Models.DBContext;
using infoproDigitalTechTask.Repository;
using Microsoft.EntityFrameworkCore;

namespace infoproDigitalTechTask
{
    public class Program
    {
        public static void Main(string[] args)
        {
            var builder = WebApplication.CreateBuilder(args);

            // Add services to the container.

            builder.Services.AddDbContext<QuoteManagementDBContext>(options =>
            options.UseSqlServer(builder.Configuration.GetConnectionString("QuoteManagementDBString")));

            //Dependency injection
            builder.Services.AddScoped<IRepairQuotesRepository, RepairQuotesRepository>();

            builder.Services.AddControllers();
            // Learn more about configuring Swagger/OpenAPI at https://aka.ms/aspnetcore/swashbuckle
            builder.Services.AddEndpointsApiExplorer();
            builder.Services.AddSwaggerGen();

            var app = builder.Build();

            using (var scope = app.Services.CreateScope())
            {
                var services = scope.ServiceProvider;
                try
                {
                    var context = services.GetRequiredService<QuoteManagementDBContext>();
                    DatabaseSeeder.SeedData(context);
                }
                catch (Exception ex)
                {
                    var logger = services.GetRequiredService<ILogger<Program>>();
                    logger.LogError(ex, "An error occurred while populating the database.");
                }
            }

            // Configure the HTTP request pipeline.
            if (app.Environment.IsDevelopment())
            {
                app.UseSwagger();
                app.UseSwaggerUI();
            }

            app.UseHttpsRedirection();

            app.UseAuthorization();


            app.MapControllers();

            app.Run();
        }
    }
}
